    <h2 style="text-align: center;">The Sabroso Story</h2>
    <p style="text-align: center;">The name "Sabroso" translates to "flavorful" in Spanish, and that's exactly what we strive for in every dish we create.<br>
        Our story began with Abuela Sophia, whose kitchen was the heart of our family gatherings. Her cooking wasn't just about sustenance; it was a way to connect, share stories, and celebrate life.
        Inspired by Abuela Sophia's legacy, we decided to share her passion for food with the community. Sabroso Cafe is a tribute to her timeless recipes, but with a modern twist. We use fresh,
        seasonal ingredients and innovative techniques to elevate classic dishes without compromising their essence.</p>
    <h2 style="text-align: center;">More Than Just a Meal</h2>
    <p style="text-align: center;">At Sabroso Cafe, we believe dining is an experience. We create a warm and welcoming atmosphere where you can relax, unwind, and savor delicious food in the company of loved ones.
        Our friendly staff is dedicated to providing exceptional service, ensuring your visit is a memorable one.</p>
    <h2 style="text-align: center;">A Commitment to Community</h2>
    <p>We're proud to be a part of the local community and believe in giving back.
        We source our ingredients from local farms whenever possible and support other small businesses in the area. 
        We also host community events and partner with local charities to make a positive difference.</p>
